@MappedSuperclass
public abstract class Employee {
	@Id
	@GeneratedValue
	private long id;

	private String name;

	
	private Employee() {

	}

	public Employee(String name) {
		super();
		this.name = name;
	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return "Employee [name=" + name + "]";
	}

}
